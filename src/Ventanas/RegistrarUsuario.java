    public void ingresarUsuario(){
         Usuario usuario = new Usuario();
        if (this.jComboBoxRegistrar.getSelectedIndex() == 1) {
            usuario.setId_persona(technicalFace.obtenerId_personaUsuario());
            usuario.setNombre(this.txtNombres.getText());
            usuario.setAp_materno(this.txtApMaterno.getText());
            usuario.setAp_paterno(this.txtApPaterno.getText());
            usuario.setCelular(this.txtCelular.getText());
            usuario.setTelefono(this.txtFonoFijo.getText());
            usuario.setUser(this.jTextFieldNombreUser.getText());
            usuario.setPassword(EncryptionMD5.getStringMessageDigest(txtPassword1.getText(),EncryptionMD5.MD5));
            usuario.setCorreo(this.txtCorreo1.getText());
            usuario.setDireccion(new Direccion());
            usuario.getDireccion().setRegion(this.cBoxRegion.getSelectedItem().toString());
            usuario.getDireccion().setComuna(this.cBoxComuna.getSelectedItem().toString());
            usuario.getDireccion().setProvincia(this.cBoxProvincia.getSelectedItem().toString());
            usuario.getDireccion().setCalle(this.jTextFieldCalle.getText());
            usuario.getDireccion().setNum_calle(this.jTextFieldNCalle.getText());
            usuario.getDireccion().setNum_dep(this.jTextFieldNDepar.getText());
            if (validarCamposObligatoriosUsuario().equals("Ingrese los siguientes campos:\n")) {
                if (this.validarContrasena() && this.validarEmail()){
                    if (validarCampos.validarFormtCorreo(this.txtCorreo2.getText())) {
                        if (!ruta.getText().equals("")) {
                            usuario.setDirFotoPerfil(GuardarImagenes.saveImage(ruta.getText()));
                        }else{
                            usuario.setDirFotoPerfil("no tiene foto perfil");
                        }
                        technicalFace.guardarUsuario(usuario);
                    }else{ 
                        Mensajes.error("Error! En el formato del correo");
                    }
                }else{
                   Mensajes.informacion("Las contraseñas o correos no son iguales");
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, validarCamposObligatoriosUsuario());
            }
        }
        
    }
    public String validarCamposObligatoriosUsuario(){
        String informe = "Ingrese los siguientes campos:\n";
        if (this.txtNombres.getText().equals("")) {
            informe += "\tIngrese su Nombre\n";
        }
        if (this.txtApMaterno.getText().equals("")) {
            informe+= "\tIngrese su Apellido Materno\n";
        }
        if (this.txtApPaterno.getText().equals("")) {
            informe+= "\tIngrese su Apellido Paterno\n";
        }
        if (this.cBoxRegion.getSelectedIndex() == 0) {
            informe += "\tSelccione una Región\n";
        }
        if (this.cBoxProvincia.getSelectedIndex() == 0) {
            informe += "\tSeleccione una Cuidad\n";
        }
        if(this.cBoxComuna.getSelectedIndex() == 0){
            informe += "\tSeleccione una Comuna\n";
        }
        if (this.jTextFieldCalle.getText().equals("")) {
            informe += "\tIngrese la calle donde vives\n";
        }
        if (this.jTextFieldNCalle.getText().equals("")) {
            informe += "\tIngrese el numero de la calle donde vive\n";
        }
        if (this.jTextFieldNombreUser.getText().equals("")) {
            informe+= "\tIngrese su Nombre de Usuario\n";
        }
        String contrasena = new String(this.txtPassword1.getPassword());
        if (contrasena.equals("")) {
            informe+= "\tIngrese su Contraseña\n";
        }
        String contrasena1 = new String(this.txtPassword2.getPassword());
        if (contrasena1.equals("")) {
            informe+= "\tIngrese nuevamente la Contraseña\n";
        }
        return informe;
    }
    public boolean validarContrasena(){
        return validarCampos.validarPassword(this.txtPassword1.getPassword(), this.txtPassword2.getPassword());
    }
    
    private boolean validarEmail() {
        return validarCampos.validarCorreo(this.txtCorreo1.getText(),this.txtCorreo2.getText());
    }
    public String validarCamposObligatoriosTecnicos(){
        String informe = validarCamposObligatoriosUsuario();
        if (this.jComboBoxEsp.getSelectedIndex() == 0) {
            informe += "\tSeleccione su especialidad\n";
        }
        return informe;
    }
