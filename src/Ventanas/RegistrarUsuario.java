    public void ingresarTecnico(){
        Tecnico tecnico = new Tecnico();
        if (this.jComboBoxRegistrar.getSelectedIndex() == 0) {
            tecnico.setId_persona(technicalFace.obtenerId_personaTecnico());
            tecnico.setNombre(this.txtNombres.getText());
            tecnico.setAp_materno(this.txtApMaterno.getText());
            tecnico.setAp_paterno(this.txtApPaterno.getText());
            tecnico.setCelular(this.txtCelular.getText());
            tecnico.setTelefono(this.txtFonoFijo.getText());
            tecnico.setUser(this.jTextFieldNombreUser.getText());
            tecnico.setPassword(EncryptionMD5.getStringMessageDigest(txtPassword1.getText(),EncryptionMD5.MD5));
            tecnico.setCorreo(txtCorreo1.getText());
            tecnico.setEspecialidad(this.jComboBoxEsp.getSelectedItem().toString());
            tecnico.setAnoExperencia(this.cBoxAnoExper.getSelectedItem().toString());
            tecnico.setDescripcion(this.jTextArea1.getText());
            tecnico.setDireccion(new Direccion());
            tecnico.setDirFotoPerfil(""+ruta.getText());
            tecnico.getDireccion().setRegion(this.cBoxRegion.getSelectedItem().toString());
            tecnico.getDireccion().setComuna(this.cBoxComuna.getSelectedItem().toString());
            tecnico.getDireccion().setProvincia(this.cBoxProvincia.getSelectedItem().toString());
            tecnico.getDireccion().setCalle(this.jTextFieldCalle.getText());
            tecnico.getDireccion().setNum_calle(this.jTextFieldNCalle.getText());
            tecnico.getDireccion().setNum_dep(this.jTextFieldNDepar.getText());
            if (validarCamposObligatoriosTecnicos().equals("Ingrese los siguientes campos:\n")) {
                if (validarContrasena() && validarEmail()) {
                    if (validarCampos.validarFormtCorreo(txtCorreo1.getText())) {
                        if (!ruta.getText().equals("")) {
                            tecnico.setDirFotoPerfil(GuardarImagenes.saveImage(ruta.getText()));
                        }else{
                            tecnico.setDirFotoPerfil("no tiene foto perfil");
                        }
                        technicalFace.guardarTecnico(tecnico);
                        
                        Mensajes.informacion("Felicidades se ha registrado");
                        Login login = new Login(tecnico);
                        login.setVisible(true);
                        dispose();
                    }else{
                        Mensajes.error("Error! En el formato del correo");
                    }    
     
                }else{
                    Mensajes.informacion("Las contraseñas o correos no son iguales");
                }
                
            }else{
                Mensajes.error(validarCamposObligatoriosTecnicos());
            }
        }
        
    }
